class Space {
  int id;
  String name;
  String imageUrl;
  String rate;
  String price;
  String address;
  String city;

  Space(
      this.id, this.name, this.imageUrl, this.rate, this.price, this.address, this.city) {
    id = id;
    name = name;
    rate = rate;
    price = price;
    address = address;
    city = city;
  }
}
